local function getWords()
	if vim.fn.mode() == "v" or vim.fn.mode() == "V" or vim.fn.mode() == "" then
		return tostring(vim.fn.wordcount().visual_words .. " words")
	else
		return vim.fn.wordcount().words .. " words"
	end
end

